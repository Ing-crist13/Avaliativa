    private void btnProdConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProdConsultarActionPerformed
        Categoria c = (Categoria) cmbCategoria.getSelectedItem();
        int idProduto = Integer.parseInt(txtIdProd.getText());
        ProdutoDAO pDAO = new ProdutoDAO();
  
        
        Produtos p = pDAO.getProdutos(idProduto);
        if(p==null){
            txtProdNome.setText("");
            JOptionPane.showMessageDialog(this,"Produto não encontrado");    
        }else{

            txtProdNome.setText(p.getNome());
            txtProdPreco.setText(String.valueOf(p.getPreco()));
           
            cmbCategoria.setSelectedItem(c.getIdCategoria());
            txtProdBarras.setText(String.valueOf(p.getCodBarras()));
        }
    }//GEN-LAST:event_btnProdConsultarActionPerformed

    private void btnProdEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProdEditarActionPerformed
        Categoria c = (Categoria) cmbCategoria.getSelectedItem();
        Produtos p = new Produtos();
        p.setId(Integer.parseInt(txtIdProd.getText()));
        p.setNome(txtProdNome.getText());
        p.setPreco(Double.valueOf(txtProdPreco.getText()));
        p.setCategoria(c.getIdCategoria());
        p.setCodBarras(Integer.parseInt(txtProdBarras.getText()));
       
        
       ProdutoDAO pDAO = new ProdutoDAO();
        pDAO.editar(p);
        JOptionPane.showMessageDialog(this,"Dados atualizados com sucesso");
        limparFormulario();
                                              
   
    }//GEN-LAST:event_btnProdEditarActionPerformed

    private void btnProdExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProdExcluirActionPerformed
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir? ", "Exclusão", JOptionPane.YES_NO_OPTION);
      
      if(resposta == JOptionPane.YES_OPTION){
      ProdutoDAO pDAO = new ProdutoDAO();
         pDAO.excluir(Integer.parseInt(txtIdProd.getText()));
        JOptionPane.showMessageDialog(this,"Dados excluidos com sucesso");
        limparFormulario();
      }
    }//GEN-LAST:event_btnProdExcluirActionPerformed

