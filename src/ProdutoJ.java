    private void btnProdSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProdSalvarActionPerformed
        Categoria c = (Categoria) cmbCategoria.getSelectedItem();
        Produtos p = new Produtos();
        p.setNome(txtProdNome.getText());
        p.setPreco(Double.parseDouble(txtProdPreco.getText()));
        p.setCategoria(c.getIdCategoria());
        p.setCodBarras(Integer.parseInt(txtProdBarras.getText()));
        
       ProdutoDAO pDAO = new ProdutoDAO();
        pDAO.inserir(p);
        JOptionPane.showMessageDialog(null, "Dados inseridos com sucesso");
        limparFormulario();
    }//GEN-LAST:event_btnProdSalvarActionPerformed

