    private void btnCliConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCliConsultarActionPerformed
      int idCliente = Integer.parseInt(txtCliId.getText());
        ClienteDAO cDAO = new ClienteDAO();
        
       Cliente c = cDAO.getCliente(idCliente);
        if(c==null){
            txtCliNome.setText("");
            JOptionPane.showMessageDialog(this,"Cliente não encontrado");    
        }else{

            txtCliNome.setText(c.getNome());
            txtCliEmail.setText(c.getEmail());
            txtCliCpf.setText(c.getCpf());
        }
    }//GEN-LAST:event_btnCliConsultarActionPerformed

    private void btnCliEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCliEditarActionPerformed
        Cliente c = new Cliente();
        c.setId(Integer.parseInt(txtCliId.getText()));
        c.setNome(txtCliNome.getText());
        c.setEmail(txtCliEmail.getText());
        c.setCpf(txtCliCpf.getText());
       
        
       ClienteDAO cDAO = new ClienteDAO();
        cDAO.editar(c);
        JOptionPane.showMessageDialog(this,"Dados atualizados com sucesso");
        limparFormulario();
    }//GEN-LAST:event_btnCliEditarActionPerformed

