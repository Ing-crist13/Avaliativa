    private void btnCliExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCliExcluirActionPerformed
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir? ", "Exclusão", JOptionPane.YES_NO_OPTION);
      
      if(resposta == JOptionPane.YES_OPTION){
      ClienteDAO cDAO = new ClienteDAO();
         cDAO.excluir(Integer.parseInt(txtCliId.getText()));
        JOptionPane.showMessageDialog(this,"Dados excluidos com sucesso");
        limparFormulario();
      }
    }//GEN-LAST:event_btnCliExcluirActionPerformed

    private void btnCliSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCliSalvarActionPerformed
        Cliente c = new Cliente();
        c.setNome(txtCliNome.getText());
        c.setEmail(txtCliEmail.getText());
        c.setCpf(txtCliCpf.getText());
        
        ClienteDAO estoque = new ClienteDAO();
        estoque.inserir(c);
        JOptionPane.showMessageDialog(null, "Dados inseridos com sucesso");
        limparFormulario();
    }//GEN-LAST:event_btnCliSalvarActionPerformed

