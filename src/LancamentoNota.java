     public void preencherComboCliente(){
          ClienteDAO cDao = new ClienteDAO();
           List<Cliente> listaC = cDao.getCliente();
           for(Cliente c : listaC){
               cmb_cli.addItem(c);
           }
       }

     public void preencherComboFornecedor(){
          FornecedorDAO fDao = new FornecedorDAO();
           List<Fornecedor> listaF = fDao.getFornecedor();
           for(Fornecedor f : listaF){
               cmb_fornecedor.addItem(f);
           }
       }
     
    
